export function getCategoriesForLocale(locale: Locale): Category[] {
  return rawCategories.map((c) => ({
    id: c.id,
    name: c.name[locale],
    emoji: c.emoji,
    gradient: c.gradient,
    words: c.words.map((w) => ({ text: w.text[locale], difficulty: w.difficulty, emoji: w.emoji })),
  }))
}

export function getWordPool(categoryIds: string[], difficulty: DifficultyFilter, locale: Locale): WordEntry[] {
  const selected = getCategoriesForLocale(locale).filter((c) => categoryIds.includes(c.id))
  const words = selected.flatMap((c) => c.words)
  if (difficulty === 'mixed') return words
  return words.filter((w) => w.difficulty === difficulty)
}
